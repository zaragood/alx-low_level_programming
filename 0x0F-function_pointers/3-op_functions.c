#include "3-calc.h"
/**
 * op_add - returns the sum of a and b
 * @a: first integer passed
 * @b: second integer passed
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtracts two integer
 * @a: first integer passed
 * @b: second integer passed
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that  multiply two integers
 * @a: first integer passed
 * @b: second integer passed
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two integer
 * @a: first integer passed
 * @b: second integer passed
 * Return: result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division
 * @a: first integer passed
 * @b: second integer passed
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
